static void gxm_destroy(struct ra *ra) {
    struct ra_gxm *gxm = ra->priv;
    sceGxmFinish(gxm->context);
    gxm_free(gxm->linearIndicesUid);
    gxm_free(gxm->clearVerticesUid);
    gxm_delete_shader(&gxm->clearProg, gxm->shader_patcher);

    talloc_free(ra);
}

